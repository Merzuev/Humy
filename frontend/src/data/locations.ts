// Get all countries as options with translations
export const getCountryOptions = (language: string = 'en') => {
  return Object.keys(COUNTRIES_DATA).map(countryKey => {
    const country = COUNTRIES_DATA[countryKey];
    const translatedName = country.translations[language] || country.translations.en;
    return {
      value: countryKey,
      label: `${country.flag} ${translatedName}`
    };
  }).sort((a, b) => a.label.localeCompare(b.label));
};

// Get cities for a specific country with translations
export const getCitiesForCountry = (countryKey: string, language: string = 'en') => {
  const country = COUNTRIES_DATA[countryKey];
  if (!country || !country.cities) return [];
  
  return Object.keys(country.cities).map(cityKey => {
    const city = country.cities[cityKey];
    const translatedName = city[language] || city.en;
    return {
      value: cityKey,
      label: translatedName
    };
  }).sort((a, b) => a.label.localeCompare(b.label));
};

// Search countries by name with translations
export const searchCountries = (query: string, language: string = 'en') => {
  if (!query) return getCountryOptions(language);
  
  const lowerQuery = query.toLowerCase();
  return getCountryOptions(language).filter(country => 
    country.label.toLowerCase().includes(lowerQuery)
  );
};

// Search cities by name within a country with translations
export const searchCities = (countryKey: string, query: string, language: string = 'en') => {
  const cities = getCitiesForCountry(countryKey, language);
  if (!query) return cities;
  
  const lowerQuery = query.toLowerCase();
  return cities.filter(city => 
    city.label.toLowerCase().includes(lowerQuery)
  );
};

